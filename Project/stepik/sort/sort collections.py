# отсортировать коллекцию по длинне строк

lst = ["Москва", "Тверь", "Смоленск", "Псков", "Рязань"]

cities = sorted(lst, key=len)
print(cities)
# ['Тверь', 'Псков', 'Москва', 'Рязань', 'Смоленск']

# --------------------------------------------------------------------------------
# отсортировать коллекцию по последнему символу слова (по алфавиту)

cities2 = sorted(lst, key=lambda x: x[-1])
print(cities2)
# ['Москва', 'Псков', 'Смоленск', 'Тверь', 'Рязань']

