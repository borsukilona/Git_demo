# четные элементы получат 0, нечетные - 1
# будем сортировать числа по ключу (сначала четные 0, потом нечетные 1 будут)

'''
def is_odd(x):
    return x % 2 # будет возвращать 0 (= True) для четных и 1 для нечетных (= False)
'''

a = [4, 3, -10, 1, 7, 12]

# b = sorted(a, key=is_odd)
# key применяет к каждому элементу нашу функцию и в соответствии с этим key идет сортировка (сначала 0, потом 1)

b = sorted(a, key=lambda x: x % 2)
print(b)
# [4, -10, 12, 3, 1, 7]

# --------------------------------------------------------------------------------
# будем сначала сортировать числа по ключу (сначала четные 0, потом нечетные 1 будут),
# а потом сами группы "четных" сортировать по возрастанию и группу "нечетных" сортироват по возрастанию

с = sorted(a, key=lambda x: x if x % 2 == 0 else x + 100)
print(с)
# [-10, 4, 12, 1, 3, 7]

